package apps.nixonblkmnd.reminderapplication;

public class ReminderObject {
    //VARIABLES
    private String reminderName;
    private String reminderStartDate;
    private String reminderStartTime;
    private String reminderEndDate;
    private String reminderEndTime;
    private String reminderLocation;
    private String reminderDescription;



    //NAME GETTERS SETTERS
    public String getReminderName() {
        return reminderName;
    }
    public void setReminderName(String reminderName) {
        this.reminderName = reminderName;
    }

    //START DATE GETTERS SETTERS
    public String getReminderStartDate() {
        return reminderStartDate;
    }
    public void setReminderStartDate(String reminderStartDate) {
        this.reminderStartDate = reminderStartDate;
    }

    //START TIME GETTERS SETTERS
    public String getReminderStartTime() {
        return reminderStartTime;
    }
    public void setReminderStartTime(String reminderStartTime) {
        this.reminderStartTime = reminderStartTime;
    }

    //END DATE GETTERS SETTERS
    public String getReminderEndDate() {
        return reminderEndDate;
    }
    public void setReminderEndDate(String reminderEndDate) {
        this.reminderEndDate = reminderEndDate;
    }

    //END TIME GETTERS SETTERS
    public String getReminderEndTime() {
        return reminderEndTime;
    }
    public void setReminderEndTime(String reminderEndTime) {
        this.reminderEndTime = reminderEndTime;
    }

    //LOCATION GETTERS SETTERS
    public String getReminderLocation() {
        return reminderLocation;
    }
    public void setReminderLocation(String reminderLocation) {
        this.reminderLocation = reminderLocation;
    }

    //DESCRIPTION GETTERS SETTERS
    public String getReminderDescription() {
        return reminderDescription;
    }
    public void setReminderDescription(String reminderDescription) {
        this.reminderDescription = reminderDescription;
    }



}
